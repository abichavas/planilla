    private void cargarComboBox() {

        String[] tipos = {"Primario", "Secundario", "Universitario"};
        this.cbGrado.setModel(new DefaultComboBoxModel<>(tipos));
        ArrayList<String> items = new ArrayList<>();

        for (clsPuesto puesto : servicioPuesto.getPuestos()) {
            items.add(puesto.getNombre());
        }

        this.cbPuesto.setModel(new DefaultComboBoxModel(items.toArray()));
    }

    private void cargarTabla() {

        DefaultTableModel model = (DefaultTableModel) tblDatosEmpleado.getModel();
        model.setRowCount(0);
        for (clsEmpleado p : this.servicioEmpleado.getEmpleados()) {

            model.addRow(new Object[]{p.getNombre(),
                p.getApellido(),
                p.getEdad(),
                p.getCedula(),
                p.getGrado(),
                p.getPuesto().getNombre(),
                p.getCantidadPensiones()});
        }
    }

