    private void cargarTabla() {

        DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();

        model.setRowCount(0);

        for (clsUsuario p : this.servicioUsuario.getUsuarios()) {

            model.addRow(new Object[]{p.getUsuario(),
                p.getPassword(),
                p.getRole(),});
        }

    }

