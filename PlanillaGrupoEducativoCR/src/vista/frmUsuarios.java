    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        TableModel model = tblUsuarios.getModel();
        ArrayList<clsUsuario> usuarios = servicioUsuario.getUsuarios();
        for (int i = 0; i < model.getRowCount(); i++) {
            clsUsuario usuario = usuarios.get(i);
            usuario.setUsuario((String) model.getValueAt(i, 0));
            usuario.setPassword((String) model.getValueAt(i, 1));
        }
        servicioUsuario.guardarUsuarios();
    }//GEN-LAST:event_jButton1ActionPerformed


